class HomePage extends StatefulWidget {
  const HomePage({super.key});

  @override
  State<HomePage> createState() => _HomePageState();
}

class _HomePageState extends State<HomePage> {
  final BehaviorSubject<String> _insertionController = BehaviorSubject()
    ..add("hello");

  @override
  void initState() {
    Timer.periodic(
      const Duration(seconds: 1),
      (_) => _insertionController.value =
          _insertionController.value + _insertionController.value,
    );
    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: Column(
        mainAxisAlignment: MainAxisAlignment.center,
        children: [
          DynamicText(listenableText: _insertionController.stream),
        ],
      ),
    );
  }
}
